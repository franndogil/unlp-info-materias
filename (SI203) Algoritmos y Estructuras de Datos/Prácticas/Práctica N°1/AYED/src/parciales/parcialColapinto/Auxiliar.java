package parciales.parcialColapinto;

public class Auxiliar {
	private double min;
	private double act;
	
	public Auxiliar(double min, double act) {
		this.min = min;
		this.act = act;
	}
	
	public void setMin(double min) {
		this.min = min;
	}
	
	public void setAct(double act) {
		this.act = act;
	}
	
	public double getMin() {
		return min;
	}
	
	public double getAct() {
		return act;
	}
}
