package parciales.grafos.primerafecha2024.parcial2;

public class Recinto {
	private String nombre;
	private int tiempo;
	
	public Recinto(String nombre, int tiempo) {
		this.nombre = nombre;
		this.tiempo = tiempo;
	}
	
	public String getNombre() {
		return nombre;
	}
	
	public int getTiempo() {
		return tiempo;
	}
}
